<?php
// -----------------------------
// إعدادات الاتصال بقاعدة البيانات
// -----------------------------

$servername = "localhost";   // اسم الخادم (عادة localhost على XAMPP)
$username   = "root";        // اسم المستخدم (افتراضي في XAMPP هو root)
$password   = "";            // كلمة المرور (افتراضي في XAMPP فارغة)
$dbname     = "universities_db"; // اسم قاعدة البيانات التي أنشأناها

// -----------------------------
// إنشاء الاتصال
// -----------------------------
$conn = new mysqli($servername, $username, $password, $dbname);

// -----------------------------
// التحقق من الاتصال
// -----------------------------
if ($conn->connect_error) {
    // إذا فشل الاتصال نعرض رسالة خطأ
    die("فشل الاتصال بقاعدة البيانات: " . $conn->connect_error);
}

// ضبط الترميز الافتراضي لدعم العربية والرموز
@mysqli_set_charset($conn, 'utf8mb4');

// إذا وصلنا هنا، الاتصال ناجح
// يمكن الآن استخدام $conn في أي صفحة لتنفيذ الاستعلامات
?>
